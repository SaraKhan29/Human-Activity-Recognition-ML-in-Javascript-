/**
  * @desc creates tensorflow graph
  * @return model
*/
function createModelFunction() 
{
  const n_hidden= 32
  const timesteps= 100
  const input_dim= 5
  const n_classes= 2
  model= tf.sequential()
  model.add(tf.layers.lstm({units: n_hidden, inputShape: [timesteps, input_dim]}));
  model.add(tf.layers.dropout(0.5));
  model.add(tf.layers.dense({units: n_classes}));

  return model;
}


